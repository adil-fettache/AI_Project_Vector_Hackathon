class ProposeDeltaLoadStrategyInput(BaseModel):
    source_app: str = Field(description="Source application name")
    entity_name: str = Field(description="Name of the source entity/table to extract")
    has_delta_field: bool = Field(
        default=False,
        description="True if the source table has a reliable last-modified or created-at timestamp column",
    )
    delta_field_name: str = Field(
        default="",
        description="Name of the delta field, e.g. 'updated_at', 'last_modified_date', 'modifiedon'",
    )
    approximate_row_count: int = Field(
        default=0,
        description="Approximate number of rows in the source table (0 = unknown)",
    )
    supports_cdc: bool = Field(
        default=False,
        description="True if the source system supports CDC (Change Data Capture) natively",
    )
    supports_api_pagination: bool = Field(
        default=True,
        description="True if the source API supports paginated queries with a date/cursor filter",
    )
    load_frequency: str = Field(
        default="daily",
        description="How often the load should run: 'hourly', 'daily', 'weekly', 'on_demand', 'near_rt'",
    )


def _propose_delta_load_strategy(
    source_app: str,
    entity_name: str,
    has_delta_field: bool = False,
    delta_field_name: str = "",
    approximate_row_count: int = 0,
    supports_cdc: bool = False,
    supports_api_pagination: bool = True,
    load_frequency: str = "daily",
) -> str:
    """Recommend a delta load strategy for extracting data from a non-SAP source."""
    logger.info(
        "propose_delta_load_strategy: source=%s entity=%s has_delta=%s cdc=%s",
        source_app, entity_name, has_delta_field, supports_cdc,
    )

    if supports_cdc:
        strategy = "CDC"
        description = (
            f"Use CDC (Change Data Capture) for {entity_name}. "
            f"Configure a CDC connector (e.g. Debezium / Kafka Connect) to stream row-level changes "
            f"into a Databricks Delta table continuously. BDC Connect / Delta Sharing can then expose "
            f"the live Delta table to Datasphere without a scheduled batch job."
        )
        notebook_hints = [
            "# CDC path: no batch extraction notebook needed",
            "# Configure CDC connector to write to Databricks Delta table",
            "# Use BDC Connect / Delta Sharing to expose table to Datasphere",
        ]
        watermark_config: dict = {}
    elif has_delta_field and delta_field_name:
        strategy = "WATERMARK_DELTA"
        description = (
            f"Use watermark-based incremental extraction for {entity_name} using field '{delta_field_name}'. "
            f"Each run reads only records where {delta_field_name} > last_successful_watermark. "
            f"Store the watermark in a Databricks Delta table (_watermarks.{entity_name.lower()}) "
            f"and update it after each successful write to HDL OUTPUTPORT."
        )
        notebook_hints = [
            "# Read last watermark",
            f"last_wm = spark.sql('SELECT MAX(watermark) FROM _watermarks.{entity_name.lower()}').collect()[0][0]",
            "",
            "# Incremental extract from source",
            f"df = source_extract(filter={{'{delta_field_name}__gt': last_wm}})",
            "",
            "# Append to OUTPUTPORT (not overwrite)",
            f"hdl_port.write(df=df, folder=Folder.OUTPUTPORT, entity='{entity_name}', mode='append')",
            "",
            "# Update watermark",
            f"new_wm = df.agg({{'{delta_field_name}': 'max'}}).collect()[0][0]",
            f"spark.sql('INSERT INTO _watermarks.{entity_name.lower()} VALUES ('{{new_wm}}')')",
        ]
        watermark_config = {
            "watermark_table": f"_watermarks.{entity_name.lower()}",
            "watermark_field": delta_field_name,
            "write_mode": "append",
            "upsert_key": "id (or primary key - confirm with user)",
        }
    elif approximate_row_count > 0 and approximate_row_count < 100_000:
        strategy = "FULL_RELOAD"
        description = (
            f"Use full reload for {entity_name} (estimated {approximate_row_count:,} rows - small enough for safe full extract). "
            f"Each run drops and rewrites the OUTPUTPORT partition. Simple and safe when volume is small."
        )
        notebook_hints = [
            f"df = source_extract_all()",
            f"hdl_port.write(df=df, folder=Folder.OUTPUTPORT, entity='{entity_name}', mode='overwrite')",
        ]
        watermark_config = {}
    else:
        strategy = "FULL_RELOAD_WITH_PAGINATION"
        description = (
            f"No delta field found and volume is large or unknown for {entity_name}. "
            f"Use paginated full reload with configurable page size. "
            f"Warning: this approach re-reads all data on every run. "
            f"Recommend adding a delta field to the source table if possible, or switching to CDC."
        )
        notebook_hints = [
            "page_size = 1000",
            "offset = 0",
            "dfs = []",
            "while True:",
            "    page = source_extract_page(offset=offset, limit=page_size)",
            "    if page.count() == 0: break",
            "    dfs.append(page)",
            "    offset += page_size",
            "df = dfs[0].unionByName(*dfs[1:]) if len(dfs) > 1 else dfs[0]",
            f"hdl_port.write(df=df, folder=Folder.OUTPUTPORT, entity='{entity_name}', mode='overwrite')",
        ]
        watermark_config = {}

    scheduling = {
        "near_rt": "Databricks Structured Streaming or CDC preferred. If batch: run every 5-15 min.",
        "hourly": "Databricks job schedule: @hourly. Cluster auto-termination: 30 min idle.",
        "daily": "Databricks job schedule: @daily at off-peak hours. Cluster auto-termination: 60 min idle.",
        "weekly": "Databricks job schedule: @weekly. Cluster auto-termination: 60 min idle.",
        "on_demand": "No schedule. Triggered manually or via API.",
    }.get(load_frequency, "Databricks job schedule: @daily")

    return json.dumps({
        "tool": "propose_delta_load_strategy",
        "source_app": source_app,
        "entity": entity_name,
        "recommended_strategy": strategy,
        "description": description,
        "notebook_pseudocode_hints": notebook_hints,
        "watermark_config": watermark_config,
        "scheduling_guidance": scheduling,
        "warnings": (
            ["No delta field found. Full reload will re-extract all data on every run. Consider adding a delta field."]
            if strategy == "FULL_RELOAD_WITH_PAGINATION" else []
        ),
    })


