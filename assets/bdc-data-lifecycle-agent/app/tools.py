# ---------------------------------------------------------------------------
# Naming convention helpers
# ---------------------------------------------------------------------------

LAYER_CODES = {"1": "Inbound/Input Port", "2": "Transformation", "3": "Output Port"}
OBJECT_TYPE_CODES = {
    "RT": "Remote Table",
    "LT": "Local Table",
    "GV": "Graphical View",
    "SV": "SQL View",
    "IL": "Intelligent Lookup",
    "DF": "Data Flow",
    "RF": "Replication Flow",
    "TF": "Transformation Flow",
    "TC": "Task Chain",
    "AC": "Data Access Control",
    "AM": "Analytical Model (Gold Data Products only)",
}
SEMANTIC_USAGE_CODES = {
    "H": "Hierarchy",
    "HD": "Hierarchy with Directory",
    "D": "Dimension",
    "T": "Text",
    "F": "Fact",
    "R": "Relational Dataset",
    "SV": "Single Values",
    "OV": "Operators and Values",
}


def _validate_artifact_name(name: str) -> dict:
    """Validate a proposed artifact name against the BDC naming convention."""
    parts = name.split("_")
    errors = []

    if len(parts) < 4:
        return {
            "valid": False,
            "errors": ["Name must have at least 4 parts separated by '_': <Layer>_<ObjectType>_<SemanticName>_<Version> or <Layer>_<ObjectType>_<SemanticUsage>_<SemanticName>_<Version>"],
        }

    layer = parts[0]
    obj_type = parts[1]
    # Detect whether semantic usage is present (3rd part is a known code and at least 5 parts exist)
    known_su = set(SEMANTIC_USAGE_CODES.keys())
    if len(parts) >= 5 and parts[2] in known_su:
        semantic_usage = parts[2]
        semantic_name = "_".join(parts[3:-1])
        version = parts[-1]
    else:
        semantic_usage = None
        semantic_name = "_".join(parts[2:-1])
        version = parts[-1]

    if layer not in LAYER_CODES:
        errors.append("Layer '" + layer + "' invalid. Must be 1, 2, or 3.")
    if obj_type not in OBJECT_TYPE_CODES:
        errors.append("ObjectType '" + obj_type + "' invalid. Must be one of: " + ", ".join(OBJECT_TYPE_CODES.keys()))
    if semantic_usage and semantic_usage not in SEMANTIC_USAGE_CODES:
        errors.append("SemanticUsage '" + semantic_usage + "' invalid. Must be one of: " + ", ".join(SEMANTIC_USAGE_CODES.keys()))
    if not semantic_name:
        errors.append("SemanticName is missing.")
    if len(version) != 2 or not version.isdigit():
        errors.append("Version '" + version + "' must be 2 digits (e.g. '01', '02').")
    # AM only allowed in layer 3
    if obj_type == "AM" and layer != "3":
        errors.append("Analytical Model (AM) is only allowed in Layer 3 (Output Port).")
    # Replication Flow and Remote Table only in layer 1
    if obj_type in ("RT", "RF") and layer != "1":
        errors.append(OBJECT_TYPE_CODES[obj_type] + " (" + obj_type + ") should be in Layer 1 (Inbound).")

    return {
        "valid": len(errors) == 0,
        "layer": LAYER_CODES.get(layer, layer),
        "object_type": OBJECT_TYPE_CODES.get(obj_type, obj_type),
        "semantic_usage": SEMANTIC_USAGE_CODES.get(semantic_usage, semantic_usage) if semantic_usage else None,
        "semantic_name": semantic_name,
        "version": version,
        "errors": errors,
    }


