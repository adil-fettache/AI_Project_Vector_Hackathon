class ClassifyNonSapIntegrationPathInput(BaseModel):
    source_app: str = Field(description="Name of the non-SAP source application, e.g. ProcessUnity, Jira, PostgreSQL, SAP Ariba")
    load_strategy: str = Field(
        description="Preferred load strategy: 'full' (full extract every run), 'delta' (incremental), or 'near_rt' (near-real-time / streaming)"
    )
    has_delta_field: bool = Field(
        default=False,
        description="True if the source table has a reliable last-modified or created-at timestamp field for delta loads",
    )
    storage_preference: str = Field(
        default="",
        description="User's preferred storage model: 'hdl' (physical ingestion into HANA Data Lake), 'zero_copy' (data stays in Databricks, accessed via Delta Sharing), or '' (undecided)",
    )
    target_consumer: str = Field(
        default="datasphere",
        description="Intended consumer of the data: 'datasphere', 'sac', 'ai_ml', 'data_product'",
    )
    has_datasphere_space: bool = Field(
        default=False,
        description="True if the target Datasphere space already exists",
    )
    security_requirements: str = Field(
        default="",
        description="Any special compliance or security requirements: 'pii', 'row_level_security', 'encryption_at_rest', 'none'",
    )


def _classify_non_sap_integration_path(
    source_app: str,
    load_strategy: str,
    has_delta_field: bool = False,
    storage_preference: str = "",
    target_consumer: str = "datasphere",
    has_datasphere_space: bool = False,
    security_requirements: str = "",
) -> str:
    """Classify whether a non-SAP integration should use the HDL+DPE path or the BDC Connect / Delta Sharing path."""
    logger.info(
        "classify_non_sap_integration_path: source=%s load=%s delta_field=%s storage_pref=%s",
        source_app, load_strategy, has_delta_field, storage_preference,
    )

    hdl_score = 0
    bdc_connect_score = 0

    if load_strategy == "full":
        hdl_score += 2
    elif load_strategy == "delta":
        hdl_score += 1
        if has_delta_field:
            bdc_connect_score += 1
    elif load_strategy == "near_rt":
        bdc_connect_score += 3

    if storage_preference == "hdl":
        hdl_score += 3
    elif storage_preference == "zero_copy":
        bdc_connect_score += 3

    if target_consumer == "ai_ml":
        bdc_connect_score += 2
    elif target_consumer in ("sac", "datasphere"):
        hdl_score += 1

    if "pii" in security_requirements or "encryption_at_rest" in security_requirements:
        hdl_score += 1
    if "row_level_security" in security_requirements:
        bdc_connect_score += 1

    if hdl_score >= bdc_connect_score:
        recommended_path = "HDL_DPE"
        reason = (
            f"Recommended: **HDL + DPE SDK path**. "
            f"Data from {source_app} will be physically ingested into HANA Data Lake (HDL) via a Databricks extraction "
            f"notebook using the DPE SDK (INBOUND -> TRANSFORMATION -> OUTPUTPORT). "
            f"Datasphere consumes from HDL via a Remote Table or Replication Flow. "
            f"This is the standard SAP BDC pattern for non-SAP sources."
        )
    else:
        recommended_path = "BDC_CONNECT"
        reason = (
            f"Recommended: **BDC Connect / Delta Sharing (zero-copy) path**. "
            f"Data from {source_app} remains in Databricks (Delta tables in Unity Catalog). "
            f"A Delta Share is created and a BDC Connect recipient is configured so Datasphere queries data "
            f"directly without physical copy. Ideal for near-RT loads, large volumes, or when data must not leave Databricks."
        )

    hdl_steps = [
        "1. Provide technical API/DB documentation for the source system.",
        "2. classify_integration_protocol: determine REST/JDBC/SFTP/GraphQL protocol from docs.",
        "3. propose_delta_load_strategy: select CDC/watermark/full_reload strategy per entity.",
        "4. identify_cluster_requirements: list Python libs, secrets, Spark config needed.",
        "5. Provide HDL config: solution_area, environment, instance_id.",
        "6. generate_databricks_notebook: full DPE SDK notebook (INBOUND->TRANSFORMATION->OUTPUTPORT).",
        "7. Review and confirm. execute_databricks_job: upload and run.",
        "8. validate_hdl_data_landing: confirm OUTPUTPORT has data.",
        "9. Phase 2C: Datasphere space, 3-layer artifacts, naming convention.",
        "10. Quality gate, Data Product registration, publication, governance, monitoring.",
    ]

    bdc_connect_steps = [
        "1. Identify Unity Catalog: catalog name, schema name, table(s) to share.",
        "2. generate_bdc_connect_config: share definition, table grants, recipient config.",
        "3. Review and confirm the Delta Share configuration.",
        "4. configure_datasphere_connection: connect Datasphere to Delta Share endpoint.",
        "5. Phase 2C: Datasphere space, 3-layer artifacts (shared Delta table is Layer 1 source).",
        "6. Quality gate, Data Product registration, publication, governance, monitoring.",
    ]

    return json.dumps({
        "tool": "classify_non_sap_integration_path",
        "source_app": source_app,
        "load_strategy": load_strategy,
        "has_delta_field": has_delta_field,
        "storage_preference": storage_preference or "undecided",
        "target_consumer": target_consumer,
        "recommended_path": recommended_path,
        "recommendation_reason": reason,
        "hdl_score": hdl_score,
        "bdc_connect_score": bdc_connect_score,
        "hdl_path_steps": hdl_steps,
        "bdc_connect_path_steps": bdc_connect_steps,
        "next_action": (
            "Call classify_integration_protocol then propose_delta_load_strategy"
            if recommended_path == "HDL_DPE"
            else "Call generate_bdc_connect_config with the Databricks Unity Catalog details"
        ),
    })


