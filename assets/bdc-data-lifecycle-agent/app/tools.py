def _propose_transformation_artifact_type(
    transformation_goal: str,
    source_artifacts: str,
    requires_scheduling: bool = False,
    requires_join: bool = True,
    requires_aggregation: bool = False,
    requires_hierarchy: bool = False,
    requires_intelligent_lookup: bool = False,
    for_sac: bool = False,
) -> str:
    """Recommend the most suitable Datasphere Layer 2 artifact type based on transformation requirements."""
    recommendations = []

    if requires_intelligent_lookup:
        recommendations.append({
            "type": "Intelligent Lookup (IL)",
            "object_type_code": "IL",
            "reason": "Intelligent Lookup is designed for fuzzy/probabilistic record matching across two datasets.",
            "naming_example": "2_IL_R_" + source_artifacts.split(",")[0].strip().upper() + "_01",
        })

    if requires_join and not requires_aggregation and not requires_scheduling:
        recommendations.append({
            "type": "Graphical View (GV)",
            "object_type_code": "GV",
            "reason": "Best for joins, projections, and filter logic without heavy transformation or scheduling needs.",
            "naming_example": "2_GV_R_<SemanticName>_01",
        })

    if requires_aggregation:
        recommendations.append({
            "type": "SQL View (SV)",
            "object_type_code": "SV",
            "reason": "SQL View allows custom aggregations, CASE logic, and complex expressions using SQL.",
            "naming_example": "2_SV_F_<SemanticName>_01",
        })

    if requires_scheduling:
        recommendations.append({
            "type": "Data Flow (DF)",
            "object_type_code": "DF",
            "reason": "Data Flows run on schedule and can move/transform data between tables, including persistence.",
            "naming_example": "2_DF_R_<SemanticName>_01",
        })
        recommendations.append({
            "type": "Transformation Flow (TF)",
            "object_type_code": "TF",
            "reason": "Transformation Flows are optimized for large-volume scheduled batch transformations into Local Tables.",
            "naming_example": "2_TF_F_<SemanticName>_01",
        })

    if requires_hierarchy:
        recommendations.append({
            "type": "Graphical View (GV) with Hierarchy semantic",
            "object_type_code": "GV",
            "reason": "Use GV with semantic usage H (Hierarchy) or HD (Hierarchy with Directory) for hierarchy models.",
            "naming_example": "2_GV_H_<SemanticName>_01",
        })

    if for_sac:
        recommendations.append({
            "type": "Analytical Model (AM) in Layer 3",
            "object_type_code": "AM",
            "reason": "Analytical Models are required for SAC consumption and expose dimensions/measures. Place in Layer 3.",
            "naming_example": "3_AM_F_<SemanticName>_01",
        })

    if not recommendations:
        recommendations.append({
            "type": "Graphical View (GV)",
            "object_type_code": "GV",
            "reason": "Default recommendation — suitable for most projection and join scenarios.",
            "naming_example": "2_GV_R_<SemanticName>_01",
        })

    return json.dumps({
        "tool": "propose_transformation_artifact_type",
        "transformation_goal": transformation_goal,
        "source_artifacts": source_artifacts.split(","),
        "recommendations": recommendations,
        "all_layer_2_options": {
            "GV": "Graphical View — joins, projections, filters",
            "SV": "SQL View — aggregations, complex SQL expressions",
            "DF": "Data Flow — scheduled data movement and transformation",
            "TF": "Transformation Flow — large-volume batch transformation to Local Tables",
            "IL": "Intelligent Lookup — fuzzy/probabilistic record matching",
            "TC": "Task Chain — orchestrate multiple artifacts in sequence",
            "AC": "Data Access Control — row-level security",
        },
        "note": "Ask the user to confirm the artifact type before calling create_datasphere_artifact.",
    })


