def _propose_datasphere_layer_structure(
    space_id: str,
    data_product_name: str,
    source_entity: str,
    transformation_goal: str,
    output_goal: str,
    is_gold_data_product: bool = False,
) -> str:
    """Propose the full 3-layer Datasphere artifact structure for a Datasphere space.

    IMPORTANT — these layers are DATASPHERE-ONLY artifact categories, NOT HDL DPE SDK folders:
    - Datasphere Layer 1 (Inbound): RT/LT artifacts reading from HDL OUTPUTPORT folder (Path A)
      or Delta Share endpoint (Path B). This is where external data enters Datasphere.
    - Datasphere Layer 2 (Transformation): GV, SV, DF, TF, IL, TC, AC for business logic.
    - Datasphere Layer 3 (Output Port): GV/SV/AM consumption-ready artifacts.

    HDL DPE SDK folders (INBOUND/TRANSFORMATION/OUTPUTPORT) are physical HANA Data Lake storage
    areas written to by Databricks notebooks — completely separate from these Datasphere layers.
    """
    safe = data_product_name.upper().replace(" ", "_").replace("-", "_")
    source_safe = source_entity.upper().replace(" ", "_").replace("-", "_")

    layer1 = [
        {
            "artifact_name": "1_RT_R_" + source_safe + "_01",
            "artifact_type": "Remote Table",
            "layer": "1 – Inbound / Input Port Layer",
            "description": "Raw source data from HDL/Databricks, accessed live via Remote Table. Replace RT with LT and add RF if persistence is required.",
            "notes": "RT = Remote Table for live access. Use RF+LT pair if replication/persistence is needed.",
        }
    ]

    layer2 = [
        {
            "artifact_name": "2_GV_R_" + safe + "_01",
            "artifact_type": "Graphical View",
            "layer": "2 – Transformation Layer",
            "description": transformation_goal,
            "notes": "Replace GV with DF (Data Flow), TF (Transformation Flow), SV (SQL View), IL (Intelligent Lookup), or TC (Task Chain) depending on transformation needs.",
        }
    ]

    layer3_type = "AM" if is_gold_data_product else "GV"
    layer3_suffix = "F" if is_gold_data_product else "R"
    layer3 = [
        {
            "artifact_name": "3_" + layer3_type + "_" + layer3_suffix + "_" + safe + "_01",
            "artifact_type": "Analytical Model" if is_gold_data_product else "Graphical View",
            "layer": "3 – Output Port Layer",
            "description": output_goal,
            "notes": (
                "AM (Analytical Model) is required for Gold Data Products consumed by SAC. "
                "For non-SAC consumption, use GV (Graphical View) or SV (SQL View)."
            ) if is_gold_data_product else (
                "Output view ready for downstream consumption. "
                "Use AM only if SAC or Analytical Dataset is required."
            ),
        }
    ]

    logger.info("propose_datasphere_layer_structure: space=%s product=%s gold=%s", space_id, data_product_name, is_gold_data_product)
    return json.dumps({
        "tool": "propose_datasphere_layer_structure",
        "space_id": space_id,
        "data_product_name": data_product_name,
        "naming_convention": "<Layer>_<ObjectType>_<SemanticUsage>_<SemanticName>_<Version>",
        "naming_reference": {
            "layers": LAYER_CODES,
            "object_types": OBJECT_TYPE_CODES,
            "semantic_usage": SEMANTIC_USAGE_CODES,
        },
        "proposed_structure": {
            "layer_1_inbound": layer1,
            "layer_2_transformation": layer2,
            "layer_3_output_port": layer3,
        },
        "important": (
            "All proposed names are starting suggestions. "
            "Ask the user to confirm or adjust the SemanticUsage, SemanticName, and Version before creating artifacts. "
            "Call validate_artifact_name for every final name before calling create_datasphere_artifact."
        ),
        "awaiting_user_confirmation": True,
    })


