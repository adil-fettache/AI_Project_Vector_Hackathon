class ProposeSapActivationPlanInput(BaseModel):
    product_id: str = Field(description="ID of the SAP-managed Data Product to activate")
    product_name: str = Field(description="Display name of the Data Product")
    target_space: str = Field(description="Datasphere space ID where the Data Product will be activated")
    sap_source_system: str = Field(description="SAP source system (e.g. 'S/4HANA', 'SuccessFactors')")
    requires_connection: bool = Field(default=True)
    requires_replication_flow: bool = Field(default=True)
    requires_transformation_flow: bool = Field(default=False)


def _propose_sap_activation_plan(
    product_id: str,
    product_name: str,
    target_space: str,
    sap_source_system: str,
    requires_connection: bool = True,
    requires_replication_flow: bool = True,
    requires_transformation_flow: bool = False,
) -> str:
    """Propose a full SAP-source activation plan and halt for user confirmation."""
    _set_confirmed("activate_sap_data_product", product_id)
    steps = []
    i = 1
    steps.append(str(i) + ". Validate target Datasphere space '" + target_space + "' exists.")
    i += 1
    if requires_connection:
        steps.append(str(i) + ". Configure Datasphere connection to " + sap_source_system + " source (credentials required).")
        i += 1
    if requires_replication_flow:
        steps.append(str(i) + ". Trigger replication flow to pull data from " + sap_source_system + " into Datasphere.")
        i += 1
    if requires_transformation_flow:
        steps.append(str(i) + ". Trigger transformation flow to model/aggregate the replicated data.")
        i += 1
    steps.append(str(i) + ". Run data quality gate. MUST PASS before activation confirmed.")
    i += 1
    steps.append(str(i) + ". Activate SAP-managed Data Product '" + product_name + "' in BDC.")
    i += 1
    steps.append(str(i) + ". Validate Data Product is available for consumption.")
    return json.dumps({
        "tool": "propose_sap_activation_plan",
        "product_id": product_id,
        "product_name": product_name,
        "target_space": target_space,
        "sap_source_system": sap_source_system,
        "plan": "\n".join(steps) + "\n\nAWAITING USER CONFIRMATION -- reply 'confirm' to proceed.",
        "requires_connection": requires_connection,
        "requires_replication_flow": requires_replication_flow,
        "requires_transformation_flow": requires_transformation_flow,
    })


