"""BDC Data Product Lifecycle Agent -- tool implementations.

Full BDC landscape scope:
  SAP path  : BDC catalog -> activate SAP-managed DP -> configure connection ->
               trigger replication/transformation flow -> validate
  Non-SAP path: classify protocol from docs -> generate Databricks notebook
               (DPE SDK, HdlPort/HdlConfig/Folder) -> get cluster requirements ->
               execute job -> land in HDL INBOUND/TRANSFORMATION/OUTPUTPORT ->
               optional Delta Share -> register & publish DP in BDC Cockpit
  Shared    : data quality gate, governance metadata, monitoring & alerting

Design rules
------------
  HITL gates: every state-changing tool requires confirmed=True or a prior
              propose_* call that flags confirmation in-memory.
  No direct SAP/Databricks/HDL API calls here -- all system calls route through
  MCP server tools injected by the agent gateway.
  DPE SDK folder convention: INBOX -> INBOUND -> TRANSFORMATION -> OUTPUTPORT
  (never Bronze/Silver/Gold).
  Milestones: M1 Landscape, M2 Source Identified, M3 Source Connected,
              M4 Data Landed / DP Activated, M5 DP Published & Monitored.
"""

