_notebooks: dict[str, str] = {}
_cluster_requirements: dict[str, dict] = {}


def _set_confirmed(action: str, resource: str) -> None:
    _confirmed_actions.add(action + ":" + resource)


def _check_confirmed(action: str, resource: str) -> bool:
    return (action + ":" + resource) in _confirmed_actions


def _clear_confirmed(action: str, resource: str) -> None:
    _confirmed_actions.discard(action + ":" + resource)


def _protocol_lib(protocol: str) -> str:
    mapping = {
        "GraphQL": "gql requests-toolbelt",
        "OData": "python-odata requests",
        "SOAP": "zeep",
        "JDBC": "(JDBC driver via Maven coordinate)",
        "SFTP": "paramiko",
        "OBJECT_STORAGE": "(native Databricks mount)",
        "SDK": "(vendor SDK -- check PyPI)",
        "REST": "requests",
    }
    return mapping.get(protocol, "requests")


