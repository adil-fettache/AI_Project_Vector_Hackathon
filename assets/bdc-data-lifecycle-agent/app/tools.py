class IdentifySourceInput(BaseModel):
    system_name: str = Field(
        description=(
            "Name of the system or application the user wants to integrate "
            "(e.g. 'Jira', 'S/4HANA', 'PostgreSQL', 'SuccessFactors', 'SAP Ariba')"
        )
    )
    business_objective: str = Field(
        description=(
            "Business goal or use-case for this integration "
            "(e.g. 'Build a Sales Order Data Product for Finance analytics')"
        )
    )


def _identify_source(system_name: str, business_objective: str) -> str:
    """Classify source as SAP or non-SAP and guide next steps."""
    sap_keywords = [
        "s/4hana", "s4hana", "s4", "successfactors", "sfsf", "ariba",
        "fieldglass", "concur", "hana cloud", "btp", "sac",
        "sap analytics cloud", "sap erp", "ecc", "bw", "bw/4hana",
        "sap datasphere", "datasphere", "sap commerce", "sap crm",
    ]
    is_sap = any(k in system_name.lower() for k in sap_keywords)
    if is_sap:
        source_category = "SAP"
        next_steps = [
            "1. Call search_bdc_data_products to find SAP-managed Data Products in the BDC catalog.",
            "2. If a relevant SAP-managed DP exists, call propose_sap_activation_plan to activate/reuse it.",
            "3. If no matching DP exists, call propose_sap_custom_integration_plan.",
            "4. For S/4HANA or SuccessFactors: configure a Datasphere connection, trigger replication flows, optionally transformation flows.",
        ]
        notes = "IMPORTANT: Always check the BDC catalog first. Prefer activating SAP-managed Data Products over creating custom ones."
    else:
        source_category = "NON_SAP"
        next_steps = [
            "1. Ask the user for technical documentation or API specs of the source system.",
            "2. Call classify_integration_protocol to determine the integration method.",
            "3. Ask the user for data scope: entities, tables, fields, filters, incremental load criteria.",
            "4. Call identify_cluster_requirements to list all Databricks dependencies.",
            "5. Call generate_databricks_notebook to create a DPE SDK-based extraction notebook.",
            "6. Present the notebook and cluster requirements to the user for approval.",
            "7. After approval, call execute_databricks_job.",
            "8. Data lands in HDL: INBOUND -> TRANSFORMATION -> OUTPUTPORT via DPE SDK.",
            "9. Call register_custom_data_product and publish_data_product to make DP available in BDC.",
        ]
        notes = (
            "IMPORTANT: Databricks is the execution layer. HDL is the landing zone. "
            "Use DPE SDK (HdlPort/HdlConfig/Folder). Never use Bronze/Silver/Gold naming."
        )
    logger.info("M2.achieved: source identified -- system=%s category=%s", system_name, source_category)
    return json.dumps({
        "tool": "identify_source",
        "system_name": system_name,
        "source_category": source_category,
        "business_objective": business_objective,
        "next_steps": next_steps,
        "notes": notes,
        "milestone": "M2",
    })


